public class Matematica {


    public static float somar(float x, float y){
        return x + y;
    }
    public static float subtrair(float x, float y){
        return x - y;
    }
    public static float multiplicar(float x, float y){
        return x * y;
    }
    public static float dividir(float x, float y){
        return x / y;
    }

    public static float mediaAritimetica(float a, float b, float c, float d){
        return (a+b+c+d)/4;
    }

    public static float mediaPonderada(float a, float b, float c, float d){
        return (a*1+b*3+c*2+d*4)/10;
    }
}
